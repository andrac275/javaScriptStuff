//Control de flujo: switch

let valor = 5

switch (valor){
    case 1:
        console.log('soy el caso 1');
        break;
    case 2:
        console.log('soy el caso 2');
        break;
    case 3:
        console.log('soy el caso 3');
        break;
    default:
        console.log('soy el DEFAULT')
}