//Control de flujo while:

let contador = 0

console.log('Contador <= 5')
while (contador <=5){
    console.log('El valor del contador es: ', contador)
    contador++
}

console.log('---------------')
contador = 0
console.log('Contador < 5')
while (contador < 5){
    console.log('El valor del contador es: ', contador)
    contador++
}

console.log('---------------')

let variable = true
while(variable){
    console.log('El valor de la variable es: ' , variable)
    variable = false
}