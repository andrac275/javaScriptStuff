//Operadores Comparacion
console.log('5 === 6' , 5 === 6)
console.log('5 == 5' , 5 == 5)
//Los strings son iguales a los numeros si el valor es el mismo
console.log('5 == "5" (string)' , 5 == '5')
//Pero con la igualdad estricta, da false.
console.log('5 === "5"' , 5 === '5')

console.log('5 < 6' , 5 < 6)
console.log('5 < 5' , 5 < 5)
console.log('5 <= 5' , 5 <= 5)

//Desigualdad
console.log('5 !== 6', 5 !== 6)
console.log('5 != "5"' , 5 != '5')





